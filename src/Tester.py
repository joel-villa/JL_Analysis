"""
Tests the behavior of a JL Reduction
"""
from Sparsification_Research.src.SSGetter import SSGetter
from scipy.linalg import norm # 2-norm by default
import numpy as np
from .plot import *

MATS = ["1138_bus"] 
#TODO: test w/ more matrices

class Tester:
    def __init__(self,jl, mats=MATS, save_fig =True, show_fig=True):
        """
        jl       - johnson lindenstrauss object
        mats     - names of test matrices
        save_fig - True -> download plots, False -> don't
        show_fig - True -> show plots, False -> don't
        """
        self.jl       = jl
        self.mats     = mats
        self.save_fig = save_fig
        self.show_fig = show_fig

    def top_left_eigenvector(self, A):
        """
        A - a (dxn) matrix where d << n

        RETURN: top left eigenvector of A

        A * A^T is a (dxd) matrix, get its top eigenvector via power iteration
        """

    def top_right_eigenvector(self, A, top_left):
        """
        A        - a (dxn) matrix where d << n
        top_left - A (dx1) vector

        RETURN: top right eigenvector

        top_left * A gives top right eigenvector of A
        """

    def diff_top_right(self, A, A_reduced):
        """
        A - (nxn) original matrix
        A_reduced - (dxn) matrix s.t. d << n, generated by JL method

        RETURN: relative difference in the top right eigenvectors of the two 
        matrices, i.e.

        |v - v_reduced| / |v|
        """
    
    def reduce(self, A, d, epsilon):
        """
        A       - (nxn) matrix
        d       - d << n
        epsilon - degree of allowed error

        RETURN: (dxn) matrix generated randomly via JL Method
        """

    def test_jl(self, A, d, epsilon):
        """
        A       - (nxn) matrix
        d       - d << n
        epsilon - degree of allowed error

        RETURN: relative difference in the top right eigenvectors of the two 
        matrices, i.e.

        |v - v_reduced| / |v|
        """

    def compare_eigenvectors(self, d_reduced, epsilon=0.5, num_iter = 10):
        """
        Test all the matrices w/ the various d_reduced values
        """
        
        jl = self.jl
        ss_getter = SSGetter(in_csr=False)
        mats = ss_getter.get_by_name(self.mats) 

        mat_dict = {}
        for name, A in mats.items():
            print(f"matrix: {name}")

            diffs = []
            for d in d_reduced:
                diff = []
                for _ in num_iter:
                    diff.append(self.test_jl(A, d, epsilon))
                diff = np.mean(diff)
                diffs.append(diff)
            mat_dict[name] = diffs
        plot1(mat_dict, d_reduced, epsilon, num_iter, self.save_fig, self.show_fig)